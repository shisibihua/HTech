package com.honghe.organization.agency.dao;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by wei on 2016/2/16.
 */
public class Agency {

    public Agency() {

    this.agencyList = new ArrayList<Agency>();
    }
    public Agency(String id, String name, String level) {
        this.id = id;
        this.name = name;
        this.level = level;
        this.agencyList = new ArrayList<Agency>();
    }

    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public List<Agency> getAgencyList() {
        return agencyList;
    }

    public void setAgencyList(List<Agency> agencyList) {
        this.agencyList = agencyList;
    }

    private String level;
    private List<Agency> agencyList;

}
