package com.honghe.user.dao.entity;


/**
 * 家长与学生的关联表
 * Created by sky on 2017/1/9.
 */
public class PcRelation {

    private long pc_id;

    private String parent_code;//家长code

    private String student_code;//学生code

    public long getPc_id() {
        return pc_id;
    }

    public void setPc_id(long pc_id) {
        this.pc_id = pc_id;
    }

    public String getParent_code() {
        return parent_code;
    }

    public void setParent_code(String parent_code) {
        this.parent_code = parent_code;
    }

    public String getStudent_code() {
        return student_code;
    }

    public void setStudent_code(String student_code) {
        this.student_code = student_code;
    }
}
