public final class Result {

    private String type;
    private int code;
    private Object value;

    public Result(String type, int code, Object value) {
        this.type = type;
        this.code = code;
        this.value = value;
    }

    public String getType() {
        return type;
    }

    public int getCode() {
        return code;
    }

    public Object getValue() {
        return value;
    }
}
